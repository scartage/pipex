#include "../../libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;
	t_byte	*str1;
	t_byte	*str2;

	str1 = (t_byte *)s1;
	str2 = (t_byte *)s2;
	i = 0;
	while (++i <= n)
	{
		if (*str1 != *str2)
			return (*str1 - *str2);
		str1++;
		str2++;
	}
	return (0);
}
